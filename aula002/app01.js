// Operadores Relacionais
/*
> --> maior
< --> menor
>= -> maior ou igual
<= -> menor ou igual
== -> igual
!= -> diferente
*/
console.log(10>20)
console.log(10==10)
console.log(10!=10)

let nome = "Guto Xavier"
/*
a variável nome também é um objeto string. Objetos possuem propriedades e métodos. Propriedades são as características de um objeto. Métodos são as ações(funções) de um objeto.
Ex:
nome.length --> propriedade
nome.toUpperCase() --> método
*/
console.log(nome.length)
console.log(nome.toUpperCase())
